class Switch
{
	public static void main(String args[])
	{
		char ch='A';
			switch(ch)
			{
				case 'A':
					System.out.println("A");
					break;
				case 65:
					System.out.println("65");
					break;
				default:
					System.out.println("Invalid choice");
					break;
			}	
	}
}